          <h3>DETAILS</h3><p> It would be conducted in two stages. First being preliminary round in which two participants will form a team and answer a question paper consisting of 30 MCQ type questions within 30 minutes. The submission time will also be noted down. The top six teams would be selected on the basis of high score and time consumed i.e. in case of a tie, the team to submit first will be selected.</p><p>
Second stage will be a ppt. Based round in which the CONCRETAGYAs would be selected. Other than this, 1st runners-up, 2nd runners-up and certificate of participation would be given. This stage will have three rounds;
Under construction- It will be the building material round consisting of six questions i.e., one question per team. A correct question will earn five points. Wrong answer will lead to the question being passed to the next team. Answering passed question will earn seven points. Only one pass is allowed on wrong answer. There is no negative marking in this round.</p><p>
<b>Lift and Load</b>- It will be the construction machinery round consisting of six questions i.e., one question per team. Each correct answer will earn five points. Wrong answer will lead to the question being passed to the next team. Answering passed question will earn seven points. Only one pass is allowed on wrong answer. There is no negative marking in this round.
</p><p>
 <b>Builders Wall</b>- It will consist of six questions based on three clues each, which may be audio-visual or descriptive type. The questions will be related to construction of famous buildings like Rashtrapati Bhawan, Eiffel Tower, Petronas Twin tower, etc. Correct answer will earn ten points and no passing is allowed.   
</p>
