          <div class="tab-pane fade in active" id="home1">
            <h3>Problem Statement</h3>
            <p>To design a manually controlled bot that can traverse given path through different terrains including various obstacles, bridges, inclines and much more than what is expected.
            </p>
          </div>
          <div class="tab-pane fade" id="paneTwo1">
           <h3>Objective</h3>
           <p>To ride your way through the narrow alleys to find a way out of the perplexing maze and to collect maximum points as you travel through it
          </p>
          </div>
          <div class="tab-pane fade" id="paneTwo2">
           <h3>Game play</h3>
           <p>1)The driver will be blindfolded before entering the arena, and thus won’t be able to see the arena       throughout the competition.
2)The driver will be instructed by the other two team-mates (instructors) to find a way out of the grid.
3)The event comprises of two levels, the details of which are as follows:-
1st level:
1.All participants will compete in the first level.
2.Maze will be full of bumpy roads, flip doors and obstacles and your aim is to complete the arena in minimum time
3.efficiently and to reach the exit.
Teams will be ranked according to the time taken for completing the arena.
2nd level:
1. In 2nd level selected teams from the 1st level will compete in this level.
2. Competitors of this level won’t have any prior idea of the arena and won’t be allowed to see the arena before their turn as it will be kept in the closed tent.
3. The path may contains bumpers, tumbling rollers, flips, bridge, false paths, sliding, etc. Also there will be speed breakers followed by sharp turns.
4.There are some checkpoints on the path. If you reach those points, you get benefit /advantage of some bonus time. Value of bonus time will vary for different checkpoints depending upon difficulty level.
5.Participants will have to complete the track within maximum 10 minutes. 
6. Scores will be calculated as  
                               SCORE=  {20*60-time taken(in 1st round +2nd round)-bonus points}*5
</p>
          </div>
          <div class="tab-pane fade" id="paneTwo3">
           <h3>Rules</h3>
           <p><ol start="1"><li> Arena should not be damaged in any circumstances. Doing so may lead to disqualification.
</li><li>. If participants calls for any kind of argument it will be sorted properly but only after completion of all rounds, in any circumstances rounds will not be interrupted.
</li><li>. Any part of the bot should not detach / be left in the arena. Pulling wires to handle the bot would result in disqualification.
</li><li>.Disputes if any will be resolved by the Organizers and his decision will be final. Event managers can revise the rule at any point of time if it is necessary.
</li><li>.Team must adhere to the spirit of healthy competition.
</li><li>.Focus is on developing robots and innovation. Teams may be inquired about the developmental process of their robot and general awareness of robotics.
</li><li>.Participants will be given time slots according to their arrival at arena venue.
</li><li>.Each team will have to report sharply at their allotted time slots otherwise will have to wait until all other participants complete their turn.
</li><li>.At the time of reporting, all team members should be present with working bot. Any kind of repairing is not allowed inside arena. If bot stops working properly, team members will have to leave the arena. Coordinators will decide if second chance is to be given or not.</li></ol>
</p>
          </div>
          <div class="tab-pane fade" id="paneTwo4">
          <h3>Technical rules and regulations</h3>
          <p>Technical rules and regulations:
1.Participants need to properly insulate their motors.
2.The bot will initially start from the entry line and in its way it will cross certain checkpoints as it reaches the finish line.
3.If the bot gets stuck at any point and needs manual intervention then it will make the bot to restart from the last checkpoint crossed. Only coordinators can touch the bot in that case, team members cannot touch the bot during its run.
4.The arena will be modified in the 2nd where some surprise elements will be added.
</p>
          </div>
           <div class="tab-pane fade" id="paneTwo5">
          <h3>Arena</h3>
         	<p>1.Arena will be made up of red bricks.
2.There will be different arenas for two levels.
3.Arena dimensions :- Outer Dimensions of arena:
4.Path width : - 350 mm. Wall Width: 90 mm.
5.Wall height : - 110 mm.
6.Flip door : - length: 350mm Height: 110 mm. Width: 10 mm.
7.Material : - Plywood/ bricks. 
</p>
          </div>
          <div class="tab-pane fade" id="paneTwo6">
          <h3>Team Specification:</h3>
         <p>
A team can consist of maximum 3 participants. Students from different educational institutes can form a team.
1.Bot Specification:-
Maximum bot size 250mmX250mmX250mm ( l*b*h).
Maximum weight : 5 Kg.
2.Maximum voltage at any point: 12 volts (+2 volts). No current limit.
3.The manual bot can be controlled wired or wirelessly.
4.Teams can use on-board power supply or external power supply.
5.The use of standard chassis, readymade Lego kits is allowed.
6.The length of connecting wire should be up to 3.5 m.
</p>
          </div>
          <div class="tab-pane fade" id="paneTwo7">
          <h3>Judging Criteria</h3>
         	<p>
            <ol type="1">
            <li>.Scoring will be on the basis of TIME required to complete the task.</li>
			<li>.Also the teams will get the benefit of the checkpoints. The point system will change accordingly in the further round of the event.</li>
			<li>.Scores will be calculated as  
                              <p> SCORE=  {20*60-time taken(in 1st round +2nd round)-bonus points}*5</p>
</li>
			<li>.The time will not be stopped under any circumstances until and unless it is the fault from our part.
</li></ol></p>
