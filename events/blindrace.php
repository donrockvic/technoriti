           <h3>RULES</h3>
           <h4>DIMENSIONS</h4>
   <ol><li>
	1 The dimensions of the robot used for Roboclash is 25cm x 25cm (l*b) (10% tolerance allowed).
</li><li>	The maximum permissible weight for the robot is 5 kg.</li></ol>
<h4>ROBOT CONTROL REQUIREMENTS</h4>
<ol><li>	The robot should be wired or wireless. In case of wireless robot, the battery should be on-board.
</li><li>	All required measures should be taken in case of wireless robots.
</li></ol><h4>BATTERY & POWER</h4><ol><li>	The maximum voltage between terminals of the robot during the match should not exceed 12V.</li>
<li>	Batteries such as LiPo, NiCd, sealed Lead Acid, Li-ion can be used. Change of battery won’t be allowed during the match.
</li><li>	All efforts must be made to protect battery terminals from a direct short and causing a battery fire, failure to do so will cause direct disqualification.
</li><li>Use of damaged, non-leak proof batteries may lead to disqualification.</li></ol>
<p>Special care should be taken to protect the on-board batteries. If judges found that the battery is not properly protected, then team will be disqualified immediately</p>

</p>
