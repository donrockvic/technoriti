           <h3>GAME PLAY</h3>
           The game will start from the start point on the track. You have to complete the track by crossing hurdles on your way and collecting bonus time in minimum time.<br/>
There will be three round , and winner will be decided in the final round. 
           <p><ol start="1"><li>In round 1, top 6 teams will be selected on the basis of minimum time in round 1. and they will qualify for next round.</li>
<li>	In round 2, top 4 teams will be selected out of 6 teams for the final round .
<li>	In final round, top 3 teams will be awarded as winner  
</li>
</ol>
