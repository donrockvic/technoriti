          <h3> DETAILS AND RULES- </h3><p>Please adhere to following guidelines while designing you imagination!
Each design has to be submitted by a team consisting of minimum two and maximum three students. It is open to students of all branches, as we all love our railways.<ul><li>
One team can only submit one design, so give it your best try.</li><li>
The event will be held in the main building in room no. 311 or 309, as per convenience.  Two hours and thirty minutes time will be provided for completion of the whole design.</li><li>
Scale of 500mm. is to 1cm. has to be maintained.  </li><li>
Length of the coach must not exceed 23.54 m., height (from the rail track) must not exceed 4250mm. and width must not exceed 3.24 m. i.e. the LHB specification. After all, it has to run on Indian tracks. </li><li> 
It should have at least 72 berths.</li><li>
It should have axle length of Indian Broad Gauge (1676mm.) track.</li><li>
 The design should be submitted on standard ED sheet (type A2) as used in the college, which will provided by the managing committee.</li><li>
More than one sheet can be submitted, but please draw Top, Front and any one side view i.e., plan, elevation and side view. Length of the coach has to be taken parallel to V.P.</li><li>
Alaso the sectional front view has to be shown also.</li><li>
Pencils to be used are H (medium dark lines), 2H and 3H (vogue construction lines), and HB (dark lines).</li><li>
Construction material has to be mentioned. Please keep in mind that economic design would be given preference.
The suspension system, braking system and coupler type must be mentioned.</li><li>
All the submitted designs should be of AC coaches alone with ACs operating on 3-phase and 50 Hz. supply. The AC ducts has to be clearly shown along with the AC units.</li></ul>

