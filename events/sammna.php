<ul><li>
Each section must have three M.Ps. Hence, the choice of state is up to the participant but with a restriction that if any section is short of M.Ps. then the organising committee will allot that section requisite no. of M.Ps. There will be at least three ministers.</li><li>
The speaker will initiate the debate with announcement of the agenda and see to its smooth proceeding. He/ She will see to it that the decorum of the parliament is maintained and will announce the beginning of question hour.</li><li>
With the permission of the speaker the ministers will present their notion of the policy. They would develop their notion keeping in mind their respective ministry. They will underline the cooperativeness they seek from other ministries and states. The time allotted to each minister is five minutes.</li><li>
Then the M.Ps. will develop their notion and present it before the parliament. The M.P. must keep in mind the conditions prevailing in his/her state and substantiate his/her notion with examples. The time allotted to each M.P. is five minutes. They will speak in a sequential manner with one M.P. speaking from the H.R rich section, followed by another from the N.R. rich section, and finally the last one from the remaining. </li><li>
The Speaker will initiate the question round in which, the M.Ps. will ask their questions to other M.Ps. and ministers. The ministers may ask questions to M.Ps. and to their counter-part in other ministries, as well.</li>
<li>The judges will select two winner; The best orator and the best factual descriptor. The criteria for this selection will be-<ul>
