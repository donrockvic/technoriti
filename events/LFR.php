          <div class="tab-pane fade" id="paneTwo2">
           <h3>RACETRACK </h3>
           <p><ol start="1"><li> The track is laid out using white 19mm tape on a matt-black base – typically MDF and blackboard paint. Track may consist of small tiles or large boards.</li><li> The racetrack shall comprise of straight lines and arcs within the confines of the base-board(s). The base board must be large enough to contain a 1.5m start straight. </li><li> Crossovers are permitted, but will be at right-angles only. Line Followers shall not turn left or right at a crossover.</li><li> The starting line and the finishing line are to be located in the straight section of the circuit. </li><li> The track shall be straight for 25cm either side of the start-finish area, and either side of a crossover. </li><li> A corner marker shall be affixed on the left side of the track in the direction of the race at each point where the curvature of the track changes (beginning and end of the arc). 
</li><li> A track may have arcs with different curvatures linked continuously. 
</li><li> The surface of the racetrack shall normally be level, however, portions of the track may be inclined at a maximum of 5 degrees, differences in level of up to 2 mm may exist at the joints between modules, gaps of up to 2 mm may exist at the joints between modules. 
</li><li> Complaints about the grip on the track surface will not be entertained</li>
</ol>
</p>
          </div>
          <div class="tab-pane fade" id="paneTwo3">
           <h3>TIMING OF LAPS</h3>
           <ol start="1">
<li> The time it takes for a Line Follower to make the circuit of a track shall be the recorded lap time. 

</li><li> To determine the lap time of the Line Follower, the period from the time the sensor at the starting line detects part of the Line Follower body to the time the sensor at the finishing line detects part of the body of the same Line Follower shall be clocked. However, no measured lap time shall be regarded as valid unless the entire body of the Line Follower passes through the finishing line. 

</li><li>The Line Follower shall have a specified maximum number of attempts to run the track within a stipulated time limit (typically 3 to 5 minutes and from 3 to 4 attempts to be decided by the organizer on the day of the race). If the performance time limit is reached and the Line Follower is in the midst of a run, the Line Follower will be allowed to complete the run and the lap time will be valid if the run is successful. 

</li><li> Within the given performance time, the handler is allowed to replace batteries 
and/or upload new program parameters to the Line Follower. 

</li><li> Line Followers shall start anywhere within the defined start area, and shall go in the specified direction. 

</li><li> If a Line Follower leaves the white line, and has not crossed the finishing line, it will be considered to have gone off the track, and that run shall be invalid. 

</li><li>The organisers will ensure that the track is located within a typical indoor environment in so far as is feasible. No request to adjust the lighting shall be entertained. 

</li><li> The organisers may demand from handlers such explanation concerning their Line Followers as is deemed necessary, and they may use their discretion to direct handlers to give up racing, disqualify them, and take any other necessary measures. 
</li></ol>

</p>
