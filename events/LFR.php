          <h3> RULES</h3>

<ol><li>Line Followers must be self contained, and not externally operated by wire or by remote radio control during the race. 

</li><li>Except for the battery pack, the handler shall not make any addition, removal, replacement or change to the hardware of a Line Follower during a contest. It is however permissible to make minor repairs. 
</li><li> A Line Follower shall not exceed 25 cm in overall length, 25 cm in overall width and 20 cm in overall height. </li></ol>


