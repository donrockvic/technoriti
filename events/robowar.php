          <div class="tab-pane fade" id="paneTwo2">
           <h3>GAME PLAY</h3>
           <p><ol start="1"><li>Each battle will consist of three rounds.
</li><li>Each round will be of 2 minutes.
</li><li>Bot will be considered eliminated if it either falls off the arena or is immobile or cannot perform linear movement within 30 seconds
</li><li>If at the end of 3 rounds if the bots are still active and both inside the arena the result will be decided on the basis of a tie breaker
</li><li>In tiebreaker round there will be few blocks on arena and block will have to put those blocks on appropriate place, and come to their start position.
</li>
</ol>
</p>
          </div>
          <div class="tab-pane fade" id="paneTwo3">
           <h3>Rules</h3>
           <p>DIMENSIONS</p>
           <ol start="1">
<li>	The dimensions of the robot used for Roboclash is 25cm x 25cm (l*b) (10% tolerance allowed).
</li><li>	The maximum permissible weight for the robot is 5 kg.
</li><li>	The robot should not be split intentionally into components or parts during the match.</li></ol>
<p>MOBILITY</p><ol start="1">
<li>	The robot should exhibit linear motion along the arena floor during the match.
</li><li>	Jumping, flying and hopping is not allowed. The robot displaying such a motion will be disqualified.
</li></ol><p>
ROBOT CONTROL REQUIREMENTS</p><ol start="1">
<li>	The robot should be wired or wireless. In case of wireless robot, the battery should be on-board.
</li><li>	All required measures should be taken in case of wireless robots.</li></ol>
<p>BATTERY & POWER</p><ol start="1"><li>	The maximum voltage between terminals of the robot during the match should not exceed 12V.
</li><li>	Batteries such as LiPo, NiCd, sealed Lead Acid, Li-ion can be used. Change of battery won’t be allowed during the match.
</li><li>	All efforts must be made to protect battery terminals from a direct short and causing a battery fire, failure to do so will cause direct disqualification.
</li><li>	Use of damaged, non-leak proof batteries may lead to disqualification.
</li><li>	Special care should be taken to protect the on-board batteries. If judges found that the battery is not properly protected, then team will be disqualified immediately.</li></ol>
<p>WEAPONS SYSTEM</p><ol start="1">
<li>	Fire is not allowed
</li><li>	There should not be any mechanism to cut the wires of opponent bot intentionally.
</li><li>	Any weapon that can damage the arena are not allowed.</li></ol>

</p>
          </div>
          
           
         
          <div class="tab-pane fade" id="paneTwo7">
          <h3>JUDGING CRITERIA</h3>
         	<p>
            <ol start="1">
            <li>A robot is declared victorious if its opponent is immobilized.
</li><li>	A robot will be declared immobile if it cannot display linear motion of at least one inch in a timed period of 30 seconds. A robot with one side of its drive train disabled will not be counted out if it can demonstrate some degree of controlled movement. In case both the robots remain mobile after the end of the round then the winner will be decided subjectively.
</li><li>	A robot that is deemed unsafe by the judges after the match has begun will be disqualified and therefore declared the loser. The match will be immediately halted and the opponent will be awarded a win.
</li><li>	If a robot is thrown out of the arena the match will stop immediately, and the robot still inside the arena will automatically be declared as the winner.
</li><li>	If the robots become entangled with each other, the robots should be taken back to initial positions and then the match will continue.</li></ol></p><p>
NOTE : Qualification of a robot to next level is subjective and totally on the decision of the judges. A robot winning in a round against an opponent doesn’t guarantee its entrance into the next round. If the judges find the winner robot incompetent to enter into the next round, it may get disqualified. Judges can disqualify both the robots of a match from advancing to next round. All the decisions taken by the judge will be final and binding to all. Any queries afterwards will not be entertained
</p>
