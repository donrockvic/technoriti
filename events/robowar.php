           <h3>TEAM SPECIFICATIONS</h3>
           <p><ol start="1"><li>	Any team can participate in Roboclash. A team may consist of a maximum of 5 participants. These participants can be from same or different institutes. 
</li><li>	Team Name: Every team must have a name which must be unique. Organizers reserves the right to reject entries from any Team whose name it deems inappropriate, offensive or conflicting. Organizers must be notified during if a Team's name has been changed. </li></ol>
          </p>
